class Profile {
    String name;
    List<String> skills;
    String biography;
    String company;
    List<String> contactDetails;
    List<String> portfolio;
    String imageURL;

    Profile(name, skills, biography, company, contactDetails, portfolio, imageURL) {
        this.name = name;
        this.skills = skills;
        this.biography = biography;
        this.company = company;
        this.contactDetails = contactDetails;
        this.portfolio = portfolio;
        this.imageURL = imageURL;
    }


}
